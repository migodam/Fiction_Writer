export const translate = (locale: Locale, key: string, fallback?: string) => {
  return messages[locale][key] || fallback || key;
};

export const useI18n = () => {
  const locale = useUIStore((state) => state.locale);
  return useMemo(
    () => ({
      locale,
      t: (key: string, fallback?: string) => translate(locale, key, fallback),
    }),
    [locale]
  );
};
